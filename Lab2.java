
public class Lab2 {

	public static void main(String[] args) {
		// 2.1
		
		final int x = 110;
		final int y = 11;
		
		System.out.println(Integer.toBinaryString(x));
		System.out.println(Integer.toBinaryString(y));
		System.out.println(Integer.toBinaryString(x&y));
		System.out.println(Integer.toBinaryString(x|y));
		System.out.println(Integer.toBinaryString(x^y));
		System.out.println(Integer.toBinaryString(x << y));
		System.out.println(Integer.toBinaryString(x >> y));

	}

}
